def _cleanup_studio_legacy_markers(
    agent: str,
    project_root: Path,
    dry_run: bool,
    *,
    remove_cypilot: bool = False,
) -> List[str]:
    """Delete pre-rebrand `.studio-installed` integration markers for *agent*.

    Markers are well-known stub files; content gate: the file MUST start
    with `# Studio` (case-sensitive) to be eligible. Anything else
    (user-authored file at the same path) is preserved.
    """
    deleted: List[str] = []
    for rel in _legacy_cleanup_patterns_for_mode(_STUDIO_LEGACY_MARKER_PATHS.get(agent, []), remove_cypilot):
        abs_path = project_root / rel
        if not abs_path.is_file():
            continue
        try:
            content = abs_path.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError):
            continue
        # Accept every legacy install-marker header form: "# Studio",
        # "# Cypilot", "# Cyber Constructor" (and case-folded variants).
        head = content.strip().splitlines()[0] if content.strip() else ""
        head_normalized = head.lower()
        if not (
            head_normalized.startswith("# studio")
            or head_normalized.startswith("# cypilot")
            or head_normalized.startswith("# cyber constructor")
            or head_normalized.startswith("# cyber pilot")
            or head_normalized.startswith("# constructor studio")
        ):
            continue
        if dry_run:
            deleted.append(rel)
        else:
            try:
                abs_path.unlink()
                deleted.append(rel)
            except OSError as exc:
                sys.stderr.write(
                    f"warning: failed to remove legacy marker file {abs_path}: {exc}\n"
                )
    return deleted
