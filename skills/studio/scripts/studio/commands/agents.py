def _resolve_studio_root(args: argparse.Namespace, project_root: Path) -> Optional[Path]:
    """Discover the Constructor Studio root directory from CLI args or project convention."""
    studio_root = Path(args.cf_studio_root).resolve() if args.cf_studio_root else None
    if studio_root is None:
        studio_rel = _read_studio_var(project_root)
        if studio_rel:
            candidate = (project_root / studio_rel).resolve()
            if _is_studio_root(candidate):
                studio_root = candidate
        if studio_root is None:
            studio_root = _find_studio_root_from_file()
    return studio_root


