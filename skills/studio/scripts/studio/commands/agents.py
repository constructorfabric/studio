def _list_workflow_files(
    studio_root: Path,
    project_root: Optional[Path] = None,
    *,
    skip_kit_slugs: Optional[Set[str]] = None,
) -> List[KitWorkflowSkill]:
    """List workflow files from .core/workflows/ and config/kits/*/workflows/.

    Returns list of ``(filename, full_path, kit_slug, explicit_skill_id)``
    tuples. ``kit_slug`` is ``None`` for core workflows
    (``.core/workflows/``) and the kit's directory name (e.g. ``sdlc``) for
    kit-sourced workflows. Kit slug is used by ``_compute_workflow_skill_id``
    to namespace skill / command identifiers as ``cf-{kit-slug}-{workflow}``
    so kit-sourced surfaces do not collide with core ones.
    """
    seen_names: set = set()
    out: List[KitWorkflowSkill] = []

    def _scan_dir(d: Path, kit_slug: Optional[str]) -> None:
        if not d.is_dir():
            return
        try:
            for p in d.iterdir():
                if not p.is_file() or p.suffix.lower() != ".md":
                    continue
                if p.name in {"AGENTS.md", "README.md"}:
                    continue
                try:
                    head = "\n".join(p.read_text(encoding="utf-8").splitlines()[:30])
                except OSError:
                    continue
                if "type: workflow" not in head:
                    continue
                if p.name not in seen_names:
                    seen_names.add(p.name)
                    out.append((p.name, p.resolve(), kit_slug, None))
        except OSError:
            pass

    # 1. Core workflows (kit_slug = None)
    _scan_dir(core_subpath(studio_root, "workflows"), None)

    # 2. Kit workflows (registered kit roots) — kit_slug = registry slug
    registered_roots = _registered_kit_roots(studio_root, project_root)
    skip_slugs = skip_kit_slugs or set()
    for kit_slug, kit_root in sorted(registered_roots.items()):
        if kit_slug in skip_slugs:
            continue
        _scan_dir(kit_root / "workflows", kit_slug)

    out.sort(key=lambda t: t[0])
    return out


def _compute_workflow_skill_id(wf_name: str, kit_slug: Optional[str], prefix: str = "cf-") -> str:
    """Return the canonical skill / command id for a workflow.

    Rules (per the rebrand brief §1.1 "Kit command prefix"):
      * The bare ``studio`` / ``cf`` workflow remains ``cf`` (no suffix).
      * Core workflows: ``{prefix}{wf_name}`` (e.g. ``cf-generate``).
      * Kit-sourced workflows: ``{prefix}{kit-slug}-{wf_name}`` (e.g.
        ``cf-sdlc-pr-review``) so kit surfaces are namespaced and do not
        collide with core or other kits.
    """
    if wf_name == "cf":
        return "cf"
    if kit_slug:
        return f"{prefix}{kit_slug}-{wf_name}"
    return f"{prefix}{wf_name}"
