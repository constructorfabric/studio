def _has_openai_codex_agent_signal(project_root: Path) -> bool:
    """Return True when a Codex agent file points back to Studio."""
    codex_agents = project_root / ".codex" / "agents"
    if not codex_agents.is_dir():
        return False
    try:
        agent_files = list(codex_agents.iterdir())
    except OSError:
        return False
    for agent_file in agent_files:
        if not agent_file.is_file() or agent_file.suffix.lower() not in {".md", ".toml"}:
            continue
        try:
            if agent_file.stat().st_size > 1_000_000:
                continue
            content = agent_file.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError):
            continue
        if _extract_studio_follow_target(content):
            return True
    return False


