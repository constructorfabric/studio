def _resolve_config_kits(studio_root: Path, project_root: Optional[Path] = None) -> Path:
    """Resolve config/kits/ directory, with fallback to adapter dir for source repos.

    In self-hosted / source-repo mode, studio_root == project_root and
    config/ lives inside the adapter directory (e.g. .bootstrap/config/).
    """
    config_kits = config_subpath(studio_root, "kits")
    if config_kits.is_dir():
        return config_kits
    if project_root is not None:
        adapter_name = _read_studio_var(project_root)
        if adapter_name:
            adapter_config_kits = project_root / adapter_name / "config" / "kits"
            if adapter_config_kits.is_dir():
                return adapter_config_kits
    return config_kits

def _registered_kit_roots(
    _studio_root: Path,
    project_root: Optional[Path],
) -> Dict[str, Path]:
    """Return registered kit roots that stay project-relative and in-project."""
    if project_root is None:
        return {}
    cfg = load_project_config(project_root)
    if cfg is None:
        return {}
    kits = cfg.get("kits")
    if not isinstance(kits, dict):
        return {}

    project_root_resolved = project_root.resolve()
    roots: Dict[str, Path] = {}
    for slug, kit_cfg in kits.items():
        if not isinstance(kit_cfg, dict):
            continue
        raw_path = str(kit_cfg.get("path") or f"config/kits/{slug}").strip()
        resolved = _resolve_registered_project_relative_path(project_root_resolved, raw_path)
        if resolved is None or not resolved.exists():
            resolved = _resolve_registered_legacy_studio_path(
                _studio_root.resolve(),
                project_root_resolved,
                raw_path,
            )
        if resolved is not None:
            roots[str(slug)] = resolved
    return roots

