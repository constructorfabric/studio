def _process_single_agent(
    agent: str,
    project_root: Path,
    studio_root: Path,
    cfg: dict,
    cfg_path: Optional[Path],
    dry_run: bool,
    *,
    remove_cypilot: bool = False,
) -> Dict[str, Any]:
    """Process a single agent and return its result dict.

    Slim orchestrator (R3 CR-T6-013): validates the agent config, then
    dispatches to extracted helpers in the original side-effect order:
    workflows → skills → kit-workflow-skills → legacy cleanup → subagents.
    """
    agent_cfg = _ensure_agent_config(agent, cfg)
    if agent_cfg is None:
        return {
            "status": "CONFIG_ERROR",
            "message": "Agent config missing or invalid",
            "config_path": cfg_path.as_posix() if cfg_path else None,
            "agent": agent,
        }

    workflows_result = _process_workflows(
        agent, project_root, studio_root, cfg, cfg_path, dry_run,
    )
    skills_result = _process_skills(
        agent, project_root, studio_root, cfg, cfg_path, dry_run,
    )
    _merge_skills_partial(
        skills_result,
        _process_kit_workflow_skills(
            agent, project_root, studio_root, cfg, cfg_path, dry_run,
        ),
    )
    _merge_skills_partial(
        skills_result,
        _process_legacy_cleanup(
            agent, project_root, studio_root, cfg, cfg_path, dry_run,
            remove_cypilot=remove_cypilot,
        ),
    )
    public_components_result = _process_kit_public_agents_and_rules(
        agent, project_root, studio_root, dry_run,
    )
    subagents_result = _process_subagents(
        agent, project_root, studio_root, cfg, cfg_path, dry_run,
    )
    _merge_action_result(subagents_result, public_components_result["agents"])
    rules_result = public_components_result["rules"]

    # @cpt-begin:cpt-studio-algo-agent-integration-generate-shims:p1:inst-agent-result
    all_errors = (
        workflows_result.get("errors", [])
        + skills_result.get("errors", [])
        + subagents_result.get("errors", [])
        + rules_result.get("errors", [])
    )
    return _build_process_single_agent_result(
        agent,
        workflows_result,
        skills_result,
        subagents_result,
        rules_result,
        all_errors,
    )
