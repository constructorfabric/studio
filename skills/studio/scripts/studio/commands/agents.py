def _validate_manifest_subagent_entry(
    kit_slug: str,
    subagent: Dict[str, Any],
    kit_root: Path,
    kit_entry: Dict[str, Any],
    project_root: Path,
    studio_root: Path,
) -> Optional[Dict[str, Any]]:
    """Normalize a canonical-manifest subagent through the legacy agents.toml validator."""
    subagent_id = str(subagent.get("id", "")).strip()
    if not subagent_id:
        return None
    subagent_source = str(subagent.get("source", subagent.get("prompt_file", ""))).strip()
    if not subagent_source:
        return None
    generated_name = _public_component_generated_name(
        kit_slug,
        subagent_id,
        prefix_generated_name=bool(subagent.get("prefix_generated_name", True)),
    )
    info = dict(subagent)
    info["prompt_file"] = subagent_source
    entry = _validate_agent_entry(generated_name, info, kit_root, set())
    if entry is None:
        return None
    resolved_subagent_source = _resolve_registered_resource_path(
        project_root,
        studio_root,
        kit_root,
        SimpleNamespace(id=subagent_id, source=subagent_source),
        kit_entry,
        source=subagent_source,
    )
    if resolved_subagent_source is None:
        return None
    entry["prompt_source_abs"] = resolved_subagent_source.resolve()
    entry["prompt_file_abs"] = resolved_subagent_source.resolve()
    return entry


def _build_public_nested_subagent_entry(  # pylint: disable=too-many-arguments,too-many-locals
    kit_slug: str,
    subagent: Dict[str, Any],
    kit_root: Path,
    kit_entry: Dict[str, Any],
    project_root: Path,
    studio_root: Path,
    agent: str,
    *,
    allow_disabled: bool = False,
) -> Optional[Tuple[_AgentEntry, Path]]:
    """Build a validated public nested subagent entry for one target."""
    if not isinstance(subagent, dict):
        return None
    if not allow_disabled and not _nested_subagent_enabled(subagent, agent):
        return None
    entry = _validate_manifest_subagent_entry(
        kit_slug,
        subagent,
        kit_root,
        kit_entry,
        project_root,
        studio_root,
    )
    if entry is None:
        return None
    subagent_source_path = entry.get("prompt_source_abs")
    if not isinstance(subagent_source_path, Path):
        return None
    nested_target = _nested_subagent_config(subagent, agent)
    nested_name = str(entry["name"])
    resolved_values = {
        "mode": str(_subagent_value(subagent, nested_target, "mode", entry.get("mode", "readwrite")) or "readwrite"),
        "role": str(_subagent_value(subagent, nested_target, "role", entry.get("role", "any")) or "any"),
        "target": str(_subagent_value(subagent, nested_target, "target", entry.get("target", "any")) or "any"),
        "provider": str(
            _subagent_value(
                subagent,
                nested_target,
                "provider",
                entry.get("provider", "anthropic"),
            )
            or "anthropic"
        ),
        "reasoning_effort": _subagent_value(
            subagent,
            nested_target,
            "reasoning_effort",
            entry.get("reasoning_effort", None),
        ),
        "context_window": _subagent_value(subagent, nested_target, "context_window", entry.get("context_window", None)),
    }
    if not _validate_subagent_scalar_overrides(nested_name, resolved_values):
        return None
    return (
        _AgentEntry(
            id=nested_name,
            description=str(entry.get("description", "") or f"Constructor Studio {nested_name} agent"),
            source=subagent_source_path.as_posix(),
            agents=_nested_subagent_agents(subagent, nested_target),
            tools=_subagent_list(subagent, nested_target, "tools"),
            disallowed_tools=_subagent_list(subagent, nested_target, "disallowed_tools"),
            mode=resolved_values["mode"],
            isolation=bool(_subagent_value(subagent, nested_target, "isolation", entry.get("isolation", False))),
            model=str(_subagent_value(subagent, nested_target, "model", entry.get("model", "")) or ""),
            skills=_subagent_list(subagent, nested_target, "skills"),
            color=str(_subagent_value(subagent, nested_target, "color", "") or ""),
            memory_dir=str(_subagent_value(subagent, nested_target, "memory_dir", "") or ""),
            role=resolved_values["role"],
            target=resolved_values["target"],
            provider=resolved_values["provider"],
            reasoning_effort=resolved_values["reasoning_effort"],
            context_window=resolved_values["context_window"],
        ),
        subagent_source_path,
    )


def _collect_public_component_nested_entries(  # pylint: disable=too-many-locals
    ctx: "_PublicComponentCollectionContext",
    component: object,
) -> None:
    for subagent in getattr(component, "subagents", []) or []:
        nested_entry = _build_public_nested_subagent_entry(
            ctx.kit_slug,
            subagent,
            ctx.kit_root,
            ctx.kit_entry,
            ctx.project_root,
            ctx.studio_root,
            ctx.agent,
        )
        if nested_entry is None:
            continue
        entry, subagent_source_path = nested_entry
        nested_name = str(entry.id)
        identity = ("agent", nested_name)
        owner = f"{ctx.kit_slug}:{subagent_source_path.as_posix()}"
        if not _record_public_agent_owner(
            identity,
            owner,
            ctx.entry_owners,
            ctx.agent_collision_errors,
        ):
            continue
        ctx.agent_entries[nested_name] = entry


