# @cpt-begin:cpt-studio-algo-agent-integration-generate-shims:p1:inst-preserve-unverifiable-generated-file
def _preserve_unverifiable_generated_file(
    out_path: Path,
    result: Dict[str, Any],
    project_root: Path,
    *,
    marker: str = _GENERATED_MARKER,
    reason: str,
) -> bool:
    """Report a generated file that cannot be safely deleted by byte match."""
    root_resolved = project_root.resolve()
    canonical = out_path.resolve()
    try:
        canonical.relative_to(root_resolved)
    except ValueError as exc:  # pragma: no cover - path traversal defense
        raise ValueError(
            f"Output path '{out_path}' escapes project root '{project_root}' — "
            "path traversal is not allowed"
        ) from exc

    if not canonical.exists():  # pragma: no cover - defensive filesystem check
        return False
    try:
        old = canonical.read_text(encoding="utf-8")
    except (OSError, UnicodeDecodeError):  # pragma: no cover - defensive filesystem error handling
        return False
    if marker not in old:
        return False

    rel = _safe_relpath(canonical, root_resolved)
    warning = {"path": rel, "action": "preserved", "reason": reason}
    result.setdefault("warnings", []).append(warning)
    result["outputs"].append(warning)
    sys.stderr.write(
        f"WARNING: preserved generated agent output '{rel}' because "
        "the expected generated payload could not be rebuilt\n"
    )
    return True
