def _build_result(
    results: Dict[str, Any],
    agents_to_process: List[str],
    project_root: Path,
    studio_root: Path,
    cfg_path: Optional[Path],
    copy_report: dict,
    dry_run: bool,
    gitignore_action: Optional[str] = None,
) -> Dict[str, Any]:
    has_errors = any(r.get("status") != "PASS" for r in results.values())
    partial_reasons = _collect_partial_reasons(results) if has_errors else []
    result = {
        "status": "PASS" if not has_errors else "PARTIAL",
        "agents": list(agents_to_process),
        "project_root": project_root.as_posix(),
        "studio_root": studio_root.as_posix(),
        "config_path": cfg_path.as_posix() if cfg_path else None,
        "dry_run": dry_run,
        "studio_copy": copy_report,
        "results": results,
    }
    if gitignore_action:
        result["gitignore"] = gitignore_action
    if partial_reasons:
        result["partial_reasons"] = partial_reasons
    return result
