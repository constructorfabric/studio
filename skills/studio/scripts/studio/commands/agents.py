def _looks_like_generated_claude_legacy_command(
    content: str,
    *,
    expected_targets: Set[str],
    current_file: Path,
    project_root: Path,
    studio_root: Path,
) -> bool:
    stripped = _GENERATED_MARKER_RE.sub("", content).strip()
    if not stripped:
        return False
