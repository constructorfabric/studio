# @cpt-begin:cpt-studio-algo-agent-integration-generate-shims:p1:inst-extract-studio-follow-target
def _extract_studio_follow_target(content: str) -> Optional[str]:
    """Return the follow-link target if *content* is a Constructor Studio-generated routing file.

    Returns the target path string when the content contains
    ``ALWAYS open and follow `<target>``` with a Constructor Studio-owned target
    (must start with ``{cf-studio-path}/``).  Returns ``None`` otherwise.

    Note: ``@/`` (project-relative) paths are NOT accepted because any
    tool can use that prefix — it is not Constructor Studio-specific.
    """
    m = _FOLLOW_LINK_RE.search(content)
    if not m:
        return None
    target = m.group(1)
    # Accept canonical and every legacy path-variable form so cleanup of
    # pre-rebrand generator output can identify files safely.
    for prefix in (
        "{cf-studio-path}/",
        "{cf-constructor-path}/",
        "{cf-path}/",
        "{studio-path}/",
        "{studio_path}/",
        "{cypilot_path}/",
    ):
        if target.startswith(prefix):
            return target
    return None
# @cpt-end:cpt-studio-algo-agent-integration-generate-shims:p1:inst-extract-studio-follow-target


def _extract_studio_endpoint_target(content: str) -> Optional[str]:
    """Return the prompt-source target from a generated endpoint-only proxy."""
    marker = "Prompt source: "
    start = content.find(marker)
    if start == -1:
        return None
    start += len(marker)
    end = content.find(". Final prompt", start)
    if end == -1:
        return None
    target = content[start:end].strip()
    if target.startswith("{cf-studio-path}/"):
        return target
    return None


def _strip_generated_frontmatter(
    content: str,
    *,
    expected_name: Optional[str] = None,
    expected_description: Optional[str] = None,
) -> Optional[str]:
    """Strip generated markers/frontmatter and validate canonical YAML fields."""
    stripped = _GENERATED_MARKER_RE.sub("", content)
    if not stripped.startswith("---"):
        return stripped

    end = stripped.find("\n---", 3)
    if end == -1:
        return stripped

    fm: Dict[str, str] = {}
    for line in stripped[3:end].splitlines():
        if ":" not in line:
            continue
        key, _, val = line.partition(":")
        key = key.strip()
        if key:
            fm[key] = _strip_wrapping_yaml_quotes(val.strip())

    if not set(fm.keys()).issubset({"name", "description"}):
        return None
    if expected_name is not None and fm.get("name") != expected_name:
        return None
    if expected_description is not None and fm.get("description") != expected_description:
        return None
    return stripped[end + 4:]


def _pure_generated_stub_matches(stripped: str) -> bool:
    """Return True when stripped generated content matches an owned stub body."""
    nonblank = [line.strip() for line in stripped.splitlines() if line.strip()]
    if not nonblank:
        return True
    if len(nonblank) == 1:
        line = nonblank[0]
        return bool(
            _extract_studio_follow_target(line)
            or line.startswith("Constructor Studio endpoint only. Prompt source: ")
        )

    follow_target = _extract_studio_follow_target(stripped)
    if not follow_target:
        return False
    expected_protocol = [
        line.strip() for line in _follow_protocol_lines(follow_target)
        if line.strip()
    ]
    return nonblank == expected_protocol


# @cpt-begin:cpt-studio-algo-agent-integration-generate-shims:p1:inst-is-pure-studio-generated
def _is_pure_studio_generated(
    content: str,
    *,
    expected_name: Optional[str] = None,
    expected_description: Optional[str] = None,
) -> bool:
    """Return True only if *content* is a pure Constructor Studio-generated stub with no user content.

    A pure generated file consists of optional YAML frontmatter, the
    generated-file marker, and optional blank lines — nothing else.
    Files that contain any additional user-authored content are **not**
    considered pure generated and must be preserved.

    Frontmatter is compared against the canonical generated shape: Constructor Studio
    stubs only ever write ``name`` and ``description``.  Any extra key
    indicates user customisation, so the file is not treated as pure.
    When *expected_name* or *expected_description* are provided the corresponding
    frontmatter values must match exactly; a mismatch means the user edited them.
    """
    if _GENERATED_MARKER not in content and not _extract_studio_follow_target(content):
        return False
    stripped = _strip_generated_frontmatter(
        content,
        expected_name=expected_name,
        expected_description=expected_description,
    )
    return bool(stripped is not None and _pure_generated_stub_matches(stripped))
# @cpt-end:cpt-studio-algo-agent-integration-generate-shims:p1:inst-is-pure-studio-generated


# @cpt-begin:cpt-studio-algo-agent-integration-generate-shims:p1:inst-is-legacy-generator-stub
def _is_legacy_generator_stub(content: str) -> bool:
    """Return True if *content* is a pre-rebrand generator stub safe to delete.

    Looser than :func:`_is_pure_studio_generated`: accepts the original
    cypilot / cf-constructor / studio generators' extra frontmatter keys
    (``tools``, ``model``, ``isolation``, ``disable-model-invocation``, etc.).
    A file qualifies when:
        * the body (after frontmatter) reduces to the ``ALWAYS open and follow``
          directive pointing at any known studio path-template prefix, AND
        * the follow target uses a legacy template (``{cypilot_path}``,
          ``{cf-constructor-path}``, ``{cf-path}``) OR the file carries a
          known generator marker comment.

    This is intentionally narrow: only files that were demonstrably produced
    by a previous version of this toolchain are deletable. User edits to the
    body (additional lines after the follow link) preserve the file.

    Note: the marker comment is now optional. Some early-vintage generators
    wrote files with just a frontmatter + follow link (no marker), but the
    follow target itself (a legacy template) is sufficient proof.
    """
    follow_target = _extract_studio_follow_target(content)
    if not follow_target:
        return False
    uses_legacy_template = any(
        follow_target.startswith(p)
        for p in ("{cypilot_path}/", "{cf-constructor-path}/", "{cf-path}/")
    )
    # Legacy classification requires the follow target to point at a legacy
    # template prefix. The presence of the broad generator marker (which now
    # matches every vintage including current `cf agents`) is NOT sufficient
    # on its own — otherwise a freshly-generated current-vintage file with a
    # legacy-prefixed NAME (e.g. `cypilot-codegen.md` referenced by a kit's
    # agents.toml) would be misclassified as legacy and deleted, only to be
    # regenerated on the next pass — breaking idempotency.
    if not uses_legacy_template:
        return False
    stripped = _strip_generated_frontmatter(content)
    if stripped is None:
        return False
    # After stripping frontmatter + marker, the only non-blank content must
    # be lines that match the FOLLOW link, the `# /<slug>` heading (windsurf
    # workflow style), or blank lines.
    for line in stripped.splitlines():
        if not line.strip():
            continue
        if _FOLLOW_LINK_RE.search(line):
            continue
        # Allow a single H1 slug heading (`# /cypilot-analyze`, `# /cf-foo`)
        if line.strip().startswith("# /") and len(line.strip()) < 60:
            continue
        return False
    return True
# @cpt-end:cpt-studio-algo-agent-integration-generate-shims:p1:inst-is-legacy-generator-stub


_GENERATED_TOML_REQUIRED_KEYS = {"name", "description", "developer_instructions"}
_GENERATED_TOML_ALLOWED_KEYS = _GENERATED_TOML_REQUIRED_KEYS | {
    "sandbox_mode",
    "model",
    "model_reasoning_effort",
    "model_context_window",
}


def _load_toml_dict(content: str) -> Optional[dict]:
    try:
        data = tomllib.loads(content)
    except tomllib.TOMLDecodeError:
        return None
    return data if isinstance(data, dict) and data else None


def _generated_toml_target(instructions: object) -> Optional[str]:
    if not isinstance(instructions, str):
        return None
    stripped = instructions.strip()
    return _extract_studio_follow_target(stripped) or _extract_studio_endpoint_target(stripped)


def _is_valid_generated_toml_shape(
    data: object,
    *,
    allow_empty_name: bool,
    allowed_keys: Set[str],
) -> bool:
    if not isinstance(data, dict) or not data:
        return False
    keys = set(data.keys())
    if not _GENERATED_TOML_REQUIRED_KEYS.issubset(keys):
        return False
    if not keys.issubset(allowed_keys):
        return False
    name = data.get("name")
    if not isinstance(name, str) or (not allow_empty_name and not name.strip()):
        return False
    return isinstance(data.get("description"), str) and isinstance(
        data.get("developer_instructions"), str
    )


def _resolve_generated_prompt_path(
    target: str,
    studio_root: Path,
    expected_name: str,
) -> Optional[Path]:
    studio_root_resolved = studio_root.resolve()
    suffix = target[len("{cf-studio-path}/"):]
    suffix_parts = [p for p in suffix.split("/") if p not in ("", ".")]
    if any(part == ".." for part in suffix_parts):
        return None

    prompt_path = studio_root_resolved.joinpath(*suffix_parts).resolve()
    try:
        prompt_path.relative_to(studio_root_resolved)
    except ValueError:  # pragma: no cover - defense-in-depth for path traversal (.. filter is primary)
        return None
    if not prompt_path.is_file() or prompt_path.stem != expected_name:
        return None
    return prompt_path


def _render_generated_toml_extras(data: dict, extra_keys: Set[str]) -> Optional[str]:
    if not extra_keys:
        return ""

    lines = []
    for key in ("sandbox_mode", "model", "model_reasoning_effort", "model_context_window"):
        if key not in data:
            continue
        value = data[key]
        if key in ("sandbox_mode", "model", "model_reasoning_effort") and isinstance(value, str):
            lines.append(f'{key} = "{_escape_toml_basic_string(value)}"')
            continue
        if key == "model_context_window" and isinstance(value, int) and not isinstance(value, bool):
            lines.append(f"{key} = {value}")
            continue
        return None
    return "\n".join(lines)


# @cpt-begin:cpt-studio-algo-agent-integration-generate-shims:p1:inst-is-legacy-generator-toml-stub
def _is_legacy_generator_toml_stub(content: str) -> bool:
    """Return True if *content* is a pre-rebrand TOML generator stub.

    Codex subagent files use TOML, not markdown. They are recognised by:
        * a TOML generator marker comment (``# Generated by ... agents``), OR
        * a ``developer_instructions`` value containing a follow-link pointing
          at any legacy or current path template,
    AND the body contains only ``name``, ``description``, and
    ``developer_instructions`` keys.
    """
    has_legacy_follow = any(
        prefix in content
        for prefix in ("{cypilot_path}/", "{cf-constructor-path}/", "{cf-path}/")
    )
    # Mirror _is_legacy_generator_stub: classify as legacy ONLY when the
    # follow-link template prefix is itself legacy. The generator-marker
    # comment alone now matches every vintage and is therefore not a reliable
    # legacy signal.
    if not has_legacy_follow:
        return False
    data = _load_toml_dict(content)
    return bool(
        data
        and isinstance(data, dict)
        and set(data.keys()).issubset(_GENERATED_TOML_REQUIRED_KEYS)
    )
# @cpt-end:cpt-studio-algo-agent-integration-generate-shims:p1:inst-is-legacy-generator-toml-stub


# @cpt-begin:cpt-studio-algo-agent-integration-generate-shims:p1:inst-is-pure-studio-generated-toml
def _is_pure_studio_generated_toml(content: str, expected_content: Optional[str] = None) -> bool:
    """Return True when *content* is a pure generated Codex TOML subagent proxy.

    A pure generated TOML proxy must contain the TOML ownership marker and only
    the canonical top-level fields emitted by ``_render_toml_agent``. Stale
    cleanup requires a canonical expected payload; structure plus a follow-link
    is not enough to prove that marker-bearing TOML has no local edits.
    """
    if _GENERATED_MARKER_TOML not in content or expected_content is None:
        return False
    if _GENERATED_MARKER_TOML not in expected_content:
        return False
    data = _load_toml_dict(content)
    expected_data = _load_toml_dict(expected_content)
    if data is None or expected_data is None:
        return False
    if not _is_valid_generated_toml_shape(
        data,
        allow_empty_name=True,
        allowed_keys=_GENERATED_TOML_ALLOWED_KEYS,
    ):
        return False
    if not _generated_toml_target(data.get("developer_instructions")):
        return False
    return data == expected_data
# @cpt-end:cpt-studio-algo-agent-integration-generate-shims:p1:inst-is-pure-studio-generated-toml


def _is_studio_managed_toml_output(content: str) -> bool:
    """Return True when *content* matches the generated Codex TOML output shape.

    Unlike ``_is_pure_studio_generated_toml`` this is used only for ownership
    enumeration (for managed ``.gitignore`` entries), not stale cleanup. It is
    intentionally looser: the file must carry the generated marker and parse as
    the known generated TOML top-level shape, but it does not need byte-for-byte
    reconstruction.
    """
    if _GENERATED_MARKER_TOML not in content:
        return False
    data = _load_toml_dict(content)
    return bool(
        data
        and _is_valid_generated_toml_shape(
            data,
            allow_empty_name=False,
            allowed_keys=_GENERATED_TOML_ALLOWED_KEYS,
        )
    )


# @cpt-begin:cpt-studio-algo-agent-integration-generate-shims:p1:inst-expected-stale-studio-generated-toml
def _expected_stale_studio_generated_toml(
    toml_file: Path,
    content: str,
    studio_root: Path,
) -> Optional[str]:
    """Rebuild canonical stale Codex TOML when local disk state proves ownership.

    Safe cleanup requires more than the marker-bearing stale file itself.
    Reconstruct only the minimal generated form when the follow-link target
    still exists under *studio_root* and still advertises matching prompt
    metadata. Files carrying optional model tuning fields are preserved
    because those values cannot be re-derived safely for removed agents.
    """
    data = _load_toml_dict(content)
    if _GENERATED_MARKER_TOML not in content or not _is_valid_generated_toml_shape(
        data,
        allow_empty_name=False,
        allowed_keys=_GENERATED_TOML_ALLOWED_KEYS,
    ):
        return None
    assert data is not None
    extra_keys = set(data.keys()) - _GENERATED_TOML_REQUIRED_KEYS

    name = data.get("name")
    target = _generated_toml_target(data.get("developer_instructions"))
    if not _generated_toml_identity_matches(toml_file, name, target):
        return None

    prompt_path = _resolve_generated_prompt_path(target, studio_root, name)
    if prompt_path is None:
        return None

    prompt_description = _extract_frontmatter_description(prompt_path)
    description = data.get("description")
    if not _generated_toml_description_matches(description, prompt_description, target):
        return None

    rendered = _render_toml_agent(
        {
            "name": name,
            "description": description,
        },
        target,
    )
    extras_block = _render_generated_toml_extras(data, extra_keys)
    if extras_block is None:
        return None
    if extras_block:
        rendered = rendered.rstrip("\n") + "\n" + extras_block + "\n"
    return rendered
# @cpt-end:cpt-studio-algo-agent-integration-generate-shims:p1:inst-expected-stale-studio-generated-toml


def _generated_toml_identity_matches(
    toml_file: Path,
    name: object,
    target: Optional[str],
) -> bool:
    return isinstance(name, str) and bool(target) and toml_file.stem == name


def _generated_toml_description_matches(
    description: object,
    prompt_description: str,
    target: str,
) -> bool:
    if not prompt_description:
        return False
    host_description = _build_host_registration_description(prompt_description, target)
    return description in (prompt_description, host_description)


