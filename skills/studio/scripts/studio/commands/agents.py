# Directories and files to copy when Constructor Studio is external to the project.
_COPY_DIRS = ["workflows", "requirements", "schemas", "templates", "prompts", "kits", "architecture", "skills"]
_COPY_ROOT_DIRS: list[str] = []
_COPY_FILES: list = []
_CORE_SUBDIR = ".core"
_copy_ignore = shutil.ignore_patterns(
    "__pycache__", "*.pyc", ".git", ".venv", "tests", ".pytest_cache", ".coverage", "coverage.json",
)

def _ensure_studio_local(
    studio_root: Path, project_root: Path, dry_run: bool,
) -> Tuple[Path, dict]:
    """Ensure Constructor Studio files are available inside *project_root*.

    If *studio_root* is already inside *project_root*, nothing happens.
    Otherwise the relevant subset is copied into ``project_root/studio/``.

    Returns ``(effective_studio_root, copy_report)``.
    """
    # 1. Already inside project
    resolved_studio = studio_root.resolve()
    resolved_project = project_root.resolve()
    if resolved_studio == resolved_project or resolved_project in resolved_studio.parents:
        return studio_root, {"action": "none"}

    # Read actual studio directory name from AGENTS.md (e.g. .studio, cpt, studio)
    configured_name = _read_studio_var(project_root)
    local_dot = project_root / (configured_name if configured_name else "studio")

    # 2. Existing submodule
    if (local_dot / ".git").exists():
        return local_dot, {"action": "none", "reason": "existing_submodule"}

    # 3. Existing installation (.core/ layout or legacy flat layout)
    if _is_studio_root(local_dot):
        return local_dot, {"action": "none", "reason": "existing_installation"}

    # 4. Copy (dry-run keeps original root so template rendering still works)
    if dry_run:
        return studio_root, {"action": "would_copy"}

    try:
        file_count = 0
        local_dot.mkdir(parents=True, exist_ok=True)

        core_dst = local_dot / _CORE_SUBDIR
        core_dst.mkdir(parents=True, exist_ok=True)
        gen_dst = local_dot / ".gen"
        gen_dst.mkdir(parents=True, exist_ok=True)

        for dirname in _COPY_DIRS:
            src = studio_root / dirname
            if src.is_dir():
                dst = core_dst / dirname
                shutil.copytree(src, dst, ignore=_copy_ignore, dirs_exist_ok=True)
                file_count += sum(1 for _ in dst.rglob("*") if _.is_file())

        for dirname in _COPY_ROOT_DIRS:
            src = studio_root / dirname
            if src.is_dir():
                dst = local_dot / dirname
                shutil.copytree(src, dst, ignore=_copy_ignore, dirs_exist_ok=True)
                file_count += sum(1 for _ in dst.rglob("*") if _.is_file())

        for fname in _COPY_FILES:
            src = studio_root / fname
            if src.is_file():
                shutil.copy2(src, core_dst / fname)
                file_count += 1

        return local_dot, {"action": "copied", "file_count": file_count}
    except OSError as exc:
        return studio_root, {"action": "error", "message": str(exc)}
