def _find_studio_root_from_file() -> Path:
    """Probe __file__ ancestry at levels 5/6/7 for a valid studio root; fall back to deepest available."""
    resolved_file = Path(__file__).resolve()
    max_index = len(resolved_file.parents) - 1
    for _level in (5, 6, 7):
        if _level > max_index:
            continue
        _candidate = resolved_file.parents[_level]
        if _is_studio_root(_candidate):
            return _candidate
    return resolved_file.parents[min(5, max_index)]
